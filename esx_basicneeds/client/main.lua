RegisterNetEvent('esx_basicneeds:onStress')
AddEventHandler('esx_basicneeds:onStress', function(prop_name)
	if not IsAnimated then
		prop_name = prop_name or '<'
		IsAnimated = true

		Citizen.CreateThread(function()
			exports['Arp_Notifications']:ArpShowProgressBar(10000, "Tomandote una pastilla de chil ...")
			--Citizen.Wait(10000)

			local playerPed = PlayerPedId()
			local x,y,z = table.unpack(GetEntityCoords(playerPed))
			local prop = CreateObject(GetHashKey(prop_name), x, y, z + 0.2, true, true, true)
			local boneIndex = GetPedBoneIndex(playerPed, 18905)
			AttachEntityToEntity(prop, playerPed, boneIndex, 0.12, 0.028, 0.001, 10.0, 175.0, 0.0, true, true, false, true, 1, true)

			local lib, anim = 'mp_suicide', 'pill_fp'

			ESX.Streaming.RequestAnimDict(lib, function()
				local playerPed = PlayerPedId()
				local lib, anim = 'mp_suicide', 'pill_fp'
				ESX.Streaming.RequestAnimDict(lib, function()
					TaskPlayAnim(playerPed, lib, anim, 8.0, 1.0, -1, 2, 0, 0, 0, 0 )
					Citizen.Wait(2800)
					ClearPedTasks(playerPed)
					if(isIncrementalChill == false)then
						isIncrementalChill = true
						ESX.ShowNotification("Notas como tu ansiedad empieza a disminuir...")
						local stress = 1000000
						TriggerEvent("esx_status:getStatus", "stress", function(value)
							stress = value.val
						end)
						Citizen.Wait(1000)
						Citizen.CreateThread(function()
							local count = 0
							print('start incremental chillpill')
							while count < 60 * 2 and stress > 10000 do
								count = count + 1
								TriggerEvent("esx_status:remove","stress", 20000)
								Citizen.Wait(1000)
								TriggerEvent("esx_status:getStatus", "stress", function(value)
									stress = value.val
								end)
							end
							isIncrementalChill = false
							print('end incremental chillpill')
						end)
					end
				end)
			end)
		
		ExecuteCommand('e flip')
		end)
	end
end)

